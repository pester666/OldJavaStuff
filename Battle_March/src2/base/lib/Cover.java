package base.lib;

public class Cover {

    protected String name;
    protected int cover;
    protected int width;
    protected int heigth;
    protected int x;
    protected int y;
    public Cover(String name, int cover, int width, int heigth, int x, int y) {
        super();
        this.name = name;
        this.cover = cover;
        this.width = width;
        this.heigth = heigth;
        this.x = x;
        this.y = y;
    }
    
    
}
